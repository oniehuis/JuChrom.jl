"""
    AlkaneStandard

Reference data used to identify an n-alkane ladder.

`spectra` contains one `MassSpectrum` per alkane. The built-in default standard is
created from unfiltered C8-C40 robust consensus spectra with integer m/z values.
Intensities are base-peak-normalized to 100, with negative consensus values floored at
0, then rounded to four significant digits.
"""
struct AlkaneStandard{
    T1<:AbstractVector{<:AbstractMassSpectrum},
    T2<:NamedTuple
}
    name::String
    spectra::T1
    metadata::T2
end

function alkane_reference_spectrum(
    carbon::Integer,
    label::AbstractString,
    ri::Real,
    mzs::AbstractVector{<:Integer},
    relativeintensities::AbstractVector{<:Real}
)
    MassSpectrum(
        collect(Int, mzs),
        collect(Float64, relativeintensities);
        attrs=(
            ri=Float64(ri),
            order=Int(carbon),
            label=String(label),
            normalization=:base_peak,
            minrelativeintensity=0.0
        )
    )
end

