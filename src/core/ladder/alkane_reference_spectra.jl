"""
    alkanereferencespectrum(carbon::Integer) -> MassSpectrum

Return a copy of the built-in n-alkane reference spectrum for carbon number `carbon`.
"""
function alkanereferencespectrum(carbon::Integer)
    index = findfirst(s -> attrs(s).order == carbon, ALKANE_REFERENCE_SPECTRA)
    isnothing(index) && throw(ArgumentError(
        "no built-in n-alkane reference spectrum for carbon number $(carbon)"))

    deepcopy(ALKANE_REFERENCE_SPECTRA[index])
end

